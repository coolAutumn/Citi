package citi.util.cmk.Stock;

public class Company {
	
	private String sname;	//股票名称
	private String ccname;	//中文名
	private String ecname;	//英文名
	private String area;	//所属地域
	private String oname;	//曾用名
	private String trade;	//所属行业
	private String mbusiness;	//主营行业
	private String pname;	//产品名称
	private String link;	//公司网址
	private String value;
	
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getCcname() {
		return ccname;
	}
	public void setCcname(String ccname) {
		this.ccname = ccname;
	}
	public String getEcname() {
		return ecname;
	}
	public void setEcname(String ecname) {
		this.ecname = ecname;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getOname() {
		return oname;
	}
	public void setOname(String oname) {
		this.oname = oname;
	}
	public String getTrade() {
		return trade;
	}
	public void setTrade(String trade) {
		this.trade = trade;
	}
	public String getMbusiness() {
		return mbusiness;
	}
	public void setMbusiness(String mbusiness) {
		this.mbusiness = mbusiness;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
}